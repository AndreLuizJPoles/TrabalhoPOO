    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        registro = null;

        try {
            registro = daoRegistro.consultar(Integer.parseInt(txtCodigo.getText()));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Código inválido. Insira um valor numérico inteiro.", "Erro", JOptionPane.ERROR_MESSAGE);
            txtCodigo.requestFocus();
            return;
        }

        if (registro == null) {//não encontrou o objeto na BD
            btnConsultar.setEnabled(false);
            txtRegistroFuncional.setEnabled(true);
            btnPesquisarRegistroFuncional.setEnabled(true);
        } else { // encontrou o objeto na BD
            // exibir atributos
            txtRegistroFuncional.setText(Integer.toString(registro.getRecepcionista().getRegFunc()));
            lblPesquisaRegistroFuncional.setText(registro.getRecepcionista().getNome());
            txtCpfHospede.setText(registro.getHospede().getCpf());
            lblPesquisaCpfHospede.setText(registro.getHospede().getNome());
            txtNumQuarto.setText(Integer.toString(registro.getQuarto().getNumero()));
            if (registro.getQuarto().getSituacao()) {
                lblPesquisaSituacaoQuarto.setText("Reservado");
            } else {
                lblPesquisaSituacaoQuarto.setText("Livre");
            }
            txtDataEntrada.setText(DateTimeFormatter.ofPattern("dd/MM/yyyy").format(registro.getDataEntrada()));

            btnConsultar.setEnabled(false);
            txtDataSaida.requestFocus();

            if (registro.getDataSaida() == null) {
                txtDataSaida.setEnabled(true);
                btnLiberar.setEnabled(true);
                txtDataSaida.requestFocus();
            }
        }

    }//GEN-LAST:event_btnConsultarActionPerformed

    private void btnPesquisarRegistroFuncionalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarRegistroFuncionalActionPerformed
        recepcionista = daoRecepcionista.consultar(Integer.parseInt(txtRegistroFuncional.getText()));

        if (recepcionista == null) { //nao achou no bd
            JOptionPane.showMessageDialog(null, "Recepcionista não cadastrado.", "Erro", JOptionPane.ERROR_MESSAGE);
        } else { //achou no bd
            lblPesquisaRegistroFuncional.setText(recepcionista.getNome());
            
            txtRegistroFuncional.setEnabled(false);
            btnPesquisarRegistroFuncional.setEnabled(false);
            
            txtCpfHospede.setEnabled(true);
            btnPesquisarCpfHospede.setEnabled(true);
        }
    }//GEN-LAST:event_btnPesquisarRegistroFuncionalActionPerformed

