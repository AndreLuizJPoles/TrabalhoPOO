    private void btnReservarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReservarActionPerformed
            try{
                registro = new Registro(Integer.parseInt(txtCodigo.getText()), LocalDate.parse(txtDataEntrada.getText(), fmt), recepcionista);
                registro.setHospede(hospede);
                registro.setQuarto(quarto);
                registro.getQuarto().reservar();

                daoRegistro.inserir(registro);
                daoQuarto.alterar(quarto);
                
                txtCodigo.setText("");
                txtCodigo.setEnabled(false);
                txtRegistroFuncional.setText("");
                txtRegistroFuncional.setEnabled(false);
                txtCpfHospede.setText("");
                txtCpfHospede.setEnabled(false);
                txtNumQuarto.setText("");
                txtNumQuarto.setEnabled(false);
                txtDataEntrada.setText("");
                txtDataEntrada.setEnabled(false);
                lblPesquisaCpfHospede.setText("");
                lblPesquisaRegistroFuncional.setText("");
                lblPesquisaSituacaoQuarto.setText("");
                
                btnReservar.setEnabled(false);
                btnConsultar.setEnabled(true);
                txtCodigo.setEnabled(true);
                txtCodigo.requestFocus();
                                
            }catch (DateTimeParseException ex) {
                JOptionPane.showMessageDialog(null, "Data de saída inválida.", "Erro", JOptionPane.ERROR_MESSAGE);
                txtDataSaida.requestFocus();
            }
    }//GEN-LAST:event_btnReservarActionPerformed

    private void btnLiberarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLiberarActionPerformed
        try {
            LocalDate dataSaida = LocalDate.parse(txtDataSaida.getText(), fmt);

            valorDiaria = registro.getQuarto().liberar((int) ChronoUnit.DAYS.between(registro.getDataEntrada(), dataSaida));

            txtValorHospedagem.setText(Double.toString(registro.getQuarto().getTotalFaturado()));

            btnLiberar.setEnabled(false);
            txtDataSaida.setEnabled(false);

        } catch (DateTimeParseException ex) {
            JOptionPane.showMessageDialog(null, "Data de saída inválida.", "Erro", JOptionPane.ERROR_MESSAGE);
            txtDataSaida.requestFocus();
        }
    }//GEN-LAST:event_btnLiberarActionPerformed

