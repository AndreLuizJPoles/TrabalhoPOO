    private void btnPesquisarSituacaoQuartoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarSituacaoQuartoActionPerformed
        quarto = daoQuarto.consultar(Integer.parseInt(txtNumQuarto.getText()));
        
        if (quarto == null) {
            JOptionPane.showMessageDialog(null, "Quarto não cadastrado.", "Erro", JOptionPane.ERROR_MESSAGE);
        } else {
            boolean sit = quarto.getSituacao();
            if(sit){
                lblPesquisaSituacaoQuarto.setText("Ocupado");
                JOptionPane.showMessageDialog(null, "Quarto Ocupado.", "Erro", JOptionPane.ERROR_MESSAGE);
                txtNumQuarto.requestFocus();
            }
            else{
                lblPesquisaSituacaoQuarto.setText("Livre");
                
                txtNumQuarto.setEnabled(false);
                btnPesquisarSituacaoQuarto.setEnabled(false);
                
                txtDataEntrada.setEnabled(true);
                btnReservar.setEnabled(true);
            }                                
        }
    }//GEN-LAST:event_btnPesquisarSituacaoQuartoActionPerformed

